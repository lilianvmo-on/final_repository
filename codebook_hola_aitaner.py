# Funci칩n para extraer el ID desde un enlace de YouTube
def extraer_id_youtube(url):
    if "youtu.be" in url:
        return url.split("/")[-1].split("?")[0]
    elif "watch?v=" in url:
        return url.split("watch?v=")[-1].split("&")[0]
    else:
        return None

